# Output Commands: print & puts

## puts and print commands are quite similar
### puts appends a new line to the argument passed in
puts "This is a puts command."
### print keeps thins all on one line
print "This is a print command."

### in irb mode both commands after printing whatever argument they are passed
### both return nil

## To highlight the difference between puts and print, we're going to use a semicolon.
### they allow you to run multiple commands all on one line.

# puts
puts "This is a puts command."; puts "This is other puts command."

# print
print "This is a print command."; print "This is other print command."