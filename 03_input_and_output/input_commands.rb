# Input Commands

## To accept input from a user, we can use the gets command.
gets

## the program execution will stop and wait for user input. After the user presses Enter, the program will continue its execution.
## Unlike puts and print, gets actually returns the user input instead of returning nil.

### The gets command always returns a new line at the end of the input.
### .chomp is a method commonly used to trim separators

gets.chomp

