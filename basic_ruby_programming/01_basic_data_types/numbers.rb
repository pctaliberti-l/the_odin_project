# Integers and Floats

# Integers are whole numbers
# Floats are numbers that contain a decimal point

# Attenntion: when doing arithmetic with two integers, the result will aways be an integer
# If you want a more accurate answer, replace one of the integers whit a float
17 / 5.0

# Converting Number Types
# Convert an integer to a float
13.to_f

# Convert a float to an integer
13.9.to_i #=> 13

